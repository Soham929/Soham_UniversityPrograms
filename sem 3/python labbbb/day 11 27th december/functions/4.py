
def boxVolume( length = 1, width = 1, height = 1 ):
    return length * width * height

print("Default values are taken as 1 ")
print("Length is 10,Width is 20,Height is 30")
print("Volume of box is ",boxVolume(10,20,30))
print("Length is 10 Width is 20")
print("Volume of box is ",boxVolume(10,20))
print("Length is 10")
print("Volume of box is ",boxVolume(10))
